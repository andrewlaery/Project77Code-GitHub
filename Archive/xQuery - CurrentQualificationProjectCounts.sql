DROP TEMPORARY TABLE IF EXISTS TTX_STATUS_CC0;
CREATE TEMPORARY TABLE TTX_STATUS_CC0 AS
	SELECT
			Qualification ,
            NameFull ,
			COUNT(*) AS NumOfCompletedProjects
		FROM TTX_MOSTRECENTPROJECT1
		WHERE Qualification = 'Competent Communicator'
		GROUP BY namefull
        ORDER BY NumOfCompletedProjects , NameFull;
